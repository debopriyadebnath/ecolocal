export const getTranslation = (lang: string, key: string): string => {
  const keys = key.split('.');
  let value: any = translations[lang] || translations['en'];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  if (typeof value === 'string') {
    return value;
  }
  
  // If not found in specified language, try English
  if (lang !== 'en') {
    value = translations['en'];
    for (const k of keys) {
      value = value?.[k];
    }
  }
  
  return typeof value === 'string' ? value : key;
};